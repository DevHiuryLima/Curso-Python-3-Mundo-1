# Quando quero utilizar um número, não devo colocar aspas. Utilizo aspas quando na verdade quero que eles seja como
# texto, e a ideia seria de realmente juntá-los ao invés de calculá-los.

print(7 + 4)    # 11

print('7' + '4')    # 74

# Quando vamos utilizar no print uma mensagem e um número ao mesmo tempo, que são tipos diferentes. Usamos então a
# vírgula, mas não o símbolo de "+".

print('Olá', 5)     # Olá 5

